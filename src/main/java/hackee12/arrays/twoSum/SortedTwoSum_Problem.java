package hackee12.arrays.twoSum;

/**
 * Numbers array is sorted in non-decreasing order.
 * Return [i1, i2] where: numbers[i1] + numbers[i2] == target && i1 < i2.
 * There will be exactly one solution.
 * Use only constant extra space.
 */
public interface SortedTwoSum_Problem {
    int[] twoSum(int[] numbers, int target);
}
