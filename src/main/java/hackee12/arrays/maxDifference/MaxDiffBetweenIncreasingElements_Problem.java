package hackee12.arrays.maxDifference;

/**
 * For integer array nums[] return the maximum difference nums[j] - nums[i], where i < j and nums[i] < nums[j].
 * Else return -1.
 * Constraints:
 * n == nums.length
 * 2 <= n <= 1000
 * 1 <= nums[i] <= 10^9
 */
public interface MaxDiffBetweenIncreasingElements_Problem {
    int maximumDifference(int[] nums);
}
