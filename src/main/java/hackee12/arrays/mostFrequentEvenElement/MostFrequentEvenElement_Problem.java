/**
 * For integer array nums[], return the most frequent even element or -1 if there is none.
 * If there is a tie, return the smallest of the most frequent elements.
 * Constraints:
 * 1 <= nums.length <= 2000
 * 0 <= nums[i] <= 10^5
 */
public interface MostFrequentEvenElement_Problem {
    int mostFrequentEven(int[] nums);
}
