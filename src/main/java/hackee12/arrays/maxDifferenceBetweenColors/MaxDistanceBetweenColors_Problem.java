package hackee12.arrays.maxDifferenceBetweenColors;

/**
 * For integer array colors[] find max difference (j-i) between different colors (colors[j] != colors[i]).
 * Constraints:
 * n == colors.length
 * 2 <= n <= 100
 * 0 <= colors[i] <= 100
 */
public interface MaxDistanceBetweenColors_Problem {
    int maxDistance(int[] colors);
}
