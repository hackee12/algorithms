package hackee12.arrays.removeTriplesFromSorted;

public interface RemoveTriplesFromSorted {

    /**
     * Removes triples from sorted non-descending array of integers. Modifies the input array in-place.
     * Returns resulted array length.
     *
     * @param numbers input array of non-descending integers.
     * @return result array length.
     */
    int removeTriples(int[] numbers);
}
