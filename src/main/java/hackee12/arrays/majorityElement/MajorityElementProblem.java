package hackee12.arrays.majorityElement;

/**
 * For integer array nums[], return the majority element.
 * Assume the majority element always exists.
 * The majority element appears more than n / 2 times.
 * <p>
 * Constraints:
 * <p>
 * n == nums.length
 * 1 <= n <= 5 * 10^4
 * -10^9 <= nums[i] <= 10^9
 */
public interface MajorityElementProblem {
    int majorityElement(int[] nums);
}
